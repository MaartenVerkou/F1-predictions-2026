"use strict";

const APP_TABLES = [
  "users",
  "groups",
  "group_members",
  "invites",
  "responses",
  "guest_responses",
  "named_guest_profiles",
  "named_guest_group_members",
  "actuals",
  "actual_snapshots",
  "actual_snapshot_values",
  "question_settings",
  "email_verifications",
  "password_resets",
  "pending_guest_claims",
  "named_guest_recovery_tokens",
  "admin_ideas"
];

const IDENTITY_TABLES = new Set([
  "users",
  "groups",
  "invites",
  "responses",
  "guest_responses",
  "actual_snapshots",
  "email_verifications",
  "password_resets",
  "named_guest_recovery_tokens",
  "admin_ideas"
]);

const POSTGRES_SCHEMA_SQL = `
CREATE TABLE IF NOT EXISTS users (
  id INTEGER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  name TEXT NOT NULL,
  email TEXT NOT NULL UNIQUE,
  password_hash TEXT NOT NULL,
  created_at TEXT NOT NULL,
  is_verified INTEGER NOT NULL DEFAULT 0,
  verified_at TEXT,
  is_admin INTEGER NOT NULL DEFAULT 0,
  is_simulated INTEGER NOT NULL DEFAULT 0,
  hide_from_global INTEGER NOT NULL DEFAULT 0
);

CREATE TABLE IF NOT EXISTS groups (
  id INTEGER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  name TEXT NOT NULL,
  owner_id INTEGER NOT NULL,
  created_at TEXT NOT NULL,
  join_code TEXT,
  join_password_hash TEXT,
  is_public INTEGER NOT NULL DEFAULT 0,
  is_global INTEGER NOT NULL DEFAULT 0,
  rules_text TEXT,
  is_simulated INTEGER NOT NULL DEFAULT 0,
  invite_link_open INTEGER NOT NULL DEFAULT 1
);

CREATE TABLE IF NOT EXISTS group_members (
  user_id INTEGER NOT NULL,
  group_id INTEGER NOT NULL,
  role TEXT NOT NULL,
  joined_at TEXT NOT NULL,
  coupled_to_global INTEGER NOT NULL DEFAULT 1,
  PRIMARY KEY(user_id, group_id)
);

CREATE TABLE IF NOT EXISTS invites (
  id INTEGER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  group_id INTEGER NOT NULL,
  code TEXT NOT NULL UNIQUE,
  created_at TEXT NOT NULL,
  created_by INTEGER NOT NULL,
  expires_at TEXT
);

CREATE TABLE IF NOT EXISTS responses (
  id INTEGER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  user_id INTEGER NOT NULL,
  group_id INTEGER NOT NULL,
  question_id TEXT NOT NULL,
  answer TEXT NOT NULL,
  created_at TEXT NOT NULL,
  updated_at TEXT NOT NULL,
  UNIQUE(user_id, group_id, question_id)
);

CREATE TABLE IF NOT EXISTS guest_responses (
  id INTEGER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  guest_id TEXT NOT NULL,
  group_id INTEGER NOT NULL,
  question_id TEXT NOT NULL,
  answer TEXT NOT NULL,
  created_at TEXT NOT NULL,
  updated_at TEXT NOT NULL,
  UNIQUE(guest_id, group_id, question_id)
);

CREATE TABLE IF NOT EXISTS named_guest_profiles (
  guest_id TEXT PRIMARY KEY,
  display_name TEXT NOT NULL,
  source_group_id INTEGER,
  resume_token_hash TEXT,
  claim_secret_hash TEXT,
  claim_secret_mode TEXT,
  claim_secret_prompt TEXT,
  claim_secret_set_at TEXT,
  created_at TEXT NOT NULL,
  updated_at TEXT NOT NULL
);

CREATE TABLE IF NOT EXISTS named_guest_group_members (
  group_id INTEGER NOT NULL,
  guest_id TEXT NOT NULL,
  display_name TEXT NOT NULL,
  joined_at TEXT NOT NULL,
  updated_at TEXT NOT NULL,
  PRIMARY KEY(group_id, guest_id)
);

CREATE TABLE IF NOT EXISTS actuals (
  question_id TEXT PRIMARY KEY,
  value TEXT NOT NULL,
  updated_at TEXT NOT NULL
);

CREATE TABLE IF NOT EXISTS actual_snapshots (
  id INTEGER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  season INTEGER NOT NULL,
  round_number INTEGER,
  round_name TEXT,
  label TEXT NOT NULL,
  source_type TEXT NOT NULL,
  source_note TEXT,
  created_at TEXT NOT NULL,
  updated_at TEXT NOT NULL,
  created_by_user_id INTEGER,
  review_status TEXT NOT NULL DEFAULT 'reviewed',
  reviewed_at TEXT,
  reviewed_by_user_id INTEGER
);

CREATE TABLE IF NOT EXISTS actual_snapshot_values (
  snapshot_id INTEGER NOT NULL,
  question_id TEXT NOT NULL,
  value TEXT NOT NULL,
  PRIMARY KEY(snapshot_id, question_id)
);

CREATE INDEX IF NOT EXISTS idx_actual_snapshots_season_round
  ON actual_snapshots(season, round_number, created_at);
CREATE INDEX IF NOT EXISTS idx_actual_snapshot_values_snapshot
  ON actual_snapshot_values(snapshot_id);

CREATE TABLE IF NOT EXISTS question_settings (
  question_id TEXT PRIMARY KEY,
  included INTEGER NOT NULL DEFAULT 1,
  points_override TEXT,
  order_index INTEGER,
  updated_at TEXT NOT NULL
);

CREATE TABLE IF NOT EXISTS email_verifications (
  id INTEGER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  user_id INTEGER NOT NULL,
  token_hash TEXT NOT NULL,
  expires_at TEXT NOT NULL,
  created_at TEXT NOT NULL
);

CREATE TABLE IF NOT EXISTS password_resets (
  id INTEGER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  user_id INTEGER NOT NULL,
  token_hash TEXT NOT NULL UNIQUE,
  expires_at TEXT NOT NULL,
  created_at TEXT NOT NULL
);

CREATE TABLE IF NOT EXISTS pending_guest_claims (
  user_id INTEGER PRIMARY KEY,
  guest_id TEXT NOT NULL,
  created_at TEXT NOT NULL
);

CREATE TABLE IF NOT EXISTS named_guest_recovery_tokens (
  id INTEGER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  guest_id TEXT NOT NULL,
  group_id INTEGER NOT NULL,
  token_hash TEXT NOT NULL UNIQUE,
  action TEXT NOT NULL,
  target_email TEXT NOT NULL,
  target_user_id INTEGER,
  created_by_user_id INTEGER NOT NULL,
  created_at TEXT NOT NULL,
  expires_at TEXT NOT NULL,
  used_at TEXT,
  CHECK(action IN ('transfer', 'reset_secret'))
);

CREATE TABLE IF NOT EXISTS admin_ideas (
  id INTEGER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  type TEXT NOT NULL DEFAULT 'question',
  title TEXT NOT NULL,
  notes TEXT,
  status TEXT NOT NULL DEFAULT 'open',
  seed_key TEXT UNIQUE,
  created_at TEXT NOT NULL,
  updated_at TEXT NOT NULL,
  created_by_user_id INTEGER,
  updated_by_user_id INTEGER,
  CHECK(type IN ('question', 'feature', 'other')),
  CHECK(status IN ('open', 'resolved', 'ignored'))
);

CREATE INDEX IF NOT EXISTS idx_admin_ideas_status_updated
  ON admin_ideas(status, updated_at);
`;

function ensurePostgresSchema(db) {
  db.exec(POSTGRES_SCHEMA_SQL);
}

module.exports = {
  APP_TABLES,
  IDENTITY_TABLES,
  POSTGRES_SCHEMA_SQL,
  ensurePostgresSchema
};
